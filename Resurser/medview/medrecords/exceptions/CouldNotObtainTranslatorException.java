package medview.medrecords.exceptions;

/**
 *
 * $Id: CouldNotObtainTranslatorException.java,v 1.2 2008/09/09 07:49:45 oloft Exp $
 *
 */

public class CouldNotObtainTranslatorException extends Exception
{
	public CouldNotObtainTranslatorException()
	{
		super();
	}

	public CouldNotObtainTranslatorException(String message)
	{
		super(message);
	}

	public CouldNotObtainTranslatorException(Throwable cause)
	{
		super(cause);
	}

	public CouldNotObtainTranslatorException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
